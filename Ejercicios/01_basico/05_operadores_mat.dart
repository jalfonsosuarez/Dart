/**
 * Operadores matemáticos en Dart
 */


main() {

  int a = 10 + 5;     // Operador SUMA (+).
  a = 20 - 10;        // Operador RESTA (-).
  a = 10 * 2;         // Operador MULTIPLICACION (*).

  double b = 10 / 2;  // Operador DIVISION (/).

  b = 10.0 % 3;       // Operador MODULO (%), retorna el resto de la division.

  b = -b;             // Operador NEGACION (cambia el valor de la expresión).

  int c = 10 ~/3;     // Operador ENTERA (devuelve el valor entero de la división).

  int d = 1;

  d++;                // Operador INCREMENTADOR.
  d--;                // Operador DECREMENTADOR.

  d+=2;               // Operador INCREMENTADOR en 2 (puede ser cualquier valor).
  d-=2;               // Operador DECREMENTADOR en 2 (puede ser cualquier valor).

  

}